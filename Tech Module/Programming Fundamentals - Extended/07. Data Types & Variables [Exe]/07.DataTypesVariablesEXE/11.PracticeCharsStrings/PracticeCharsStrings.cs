using System;

namespace _11.PracticeCharsStrings
{
    class PracticeCharsStrings
    {
        static void Main()
        {
            string str1 = "Software University";
            char ch1 = 'B';
            char ch2 = 'y';
            char ch3 = 'e';
            string str2 = "I love programming";

            Console.WriteLine("{0}\n{1}\n{2}\n{3}\n{4}", str1, ch1, ch2, ch3, str2);
        }
    }
}
